#include "ft_printf.h"

void	ft_putnbr(int n)
{
	char	temp;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		n = n % 10;
	}
	if (n < 10)
	{
		temp = n + '0';
		write(1, &temp, 1);
	}
}
